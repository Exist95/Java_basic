package fastcampus.part2;

public class _23_PrePostOperTest {
    public static void main(String[] args) {
        int x = 10;
        System.out.println(--x); //9
        System.out.println(x++); //9
        System.out.println(x--); //10
        System.out.println(++x); //10
        System.out.println(x); //10
    }
}
